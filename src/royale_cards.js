function baseCard(card) {
  const nameZhHant = card.nameZhHant ?? card.name ?? '';
  const nameEn = card.nameEn ?? card.name ?? '';
  const nameJa = card.nameJa ?? card.name ?? '';
  return {
    bodyRadius: 18,
    effectKind: 'none',
    effectValue: 0,
    nameZhHant,
    nameEn,
    nameJa,
    nameI18n: {
      'zh-Hant': nameZhHant,
      en: nameEn,
      ja: nameJa,
    },
    ...card
  };
}

